package ua.hillel.car;

public class Wheel {
    private int size;
    private String material;

    public void setSize(int size) {
        this.size = size;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getMaterial() {
        return material;
    }

    public int getSize() {
        return size;
    }
}
